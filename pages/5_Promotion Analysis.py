insight_card(f"📈 Demand Uplift from Promotions: {uplift:.1f}%",kind="success")
insight_card("💰 Promotional campaigns contribute significantly to revenue growth and customer engagement.",kind="info")
insight_card("🎯 Focus future campaigns on high-performing promotion types and periods with strong demand response.",kind="warning")